package ex_08_Switch_Statements;

public class Lab046_Interview {
    public static void main(String[] args) {

        char code = 'C';
        switch (code)
        {
            default:
                System.out.println("Helllooooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}